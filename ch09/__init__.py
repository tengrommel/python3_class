'''
适配器模式
    在两个不同的接口之间进行匹配
    代码一次性地对一个接口到其他所有接口进行了全部匹配
    
外观模式：
    外观模式的目的是为了拥有多个组件的复杂系统提供简单的接口。
    这个系统内的对象为了完成复杂的任务和交互需求会进行各种直接的交互。
    
享元模式：
    但在开发拥有许多关联对象的大型应用程序时，关注内存是否充足可以带来巨大的收益。
    享元模式背后的基本思想：
    保证共享同一状态的对象可以同时使用该共享状态的内存。
    每个享元都没有指定状态；在任何需要对特定状态执行操作的时候，该状态就需要被代码调用，传递到该享元。
'''